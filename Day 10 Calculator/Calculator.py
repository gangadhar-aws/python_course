def add(n1,n2):
    return n1+n2
def sub(n1,n2):
    return n1-n2
def multiple(n1,n2):
    return n1*n2
def divide(n1,n2):
    return n1/n2


operations = {"+":add, "-":sub, "*":multiple, "/": divide}

def calculator():
    print(logo)
    a = int(input("Enter First Value : "))
    for i in operations:
        print(i)
    calculate = True
    while calculate:
        opt = input("Pick an operation: ")
        b = int(input("Enter Second Value: "))
        answer = operations[opt](a,b)
        print(f"{a} {opt} {b} = {answer}")

    if input(f"Type 'y' to continue calculatint with {answer}, or type 'n' to start a new calculation: ")== 'y':
        a = answer
    else:
        calculate = False
        calculator()


